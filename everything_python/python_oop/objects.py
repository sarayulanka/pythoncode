# An object is a container that contains data and functionality.

# The data represents the object at a particular moment in time. Therefore, the data of an object
#  is called the state. Python uses attributes to model the state of an object.

# The functionality represents the behaviors of an object. Python uses functions to model 
# the behaviors. When a function is associated with an object, it becomes a method of the object.

# In other words, an object is a container that contains the state and methods.

# Before creating objects, you define a class first. And from the class, you can create one 
# or more objects. The objects of a class are also called instances of a class.

# Also, it is important to remember that everything is an object in python including objects

