# Intro to Variables
# A variable is a software container used to contain data or pieces of code
# When you assign a piece of code to a variable, you can access it using the variable
# Example:
user_input = input("What's your name?  ")
print("Hello " + user_input + "!")

# When you're using variables, you can change what the variable holds
# When you assign a piece of code to a variable, you can still change it
# Value is what you call the code you assign to a variable

# Now, let's create a program we did before but this time using variables

name = input("What's your name?  ")
name_length = len(name)
print(name_length)

