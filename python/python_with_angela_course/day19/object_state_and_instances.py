# Object instances is the concept of how multiple objects
# created from the same class have no connection to each other
# For example, if I were to mkae two objects named timmy and tommy from the same turtle class,
# they would be independent of each other and have no relation even though they come from the same class

# Object state is the concept of what an object is doing at a particular time
# For example timmy is getting his color changed to purple so that is his state
# and tommy is getting his color changed to green so that it his state
# timmy and tommy's states are completely different

# State could also mean if one object is told to move forward while one is told to stay still
# These are two completely different states


